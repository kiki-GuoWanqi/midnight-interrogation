// --- Main export ---
export function generateSystemPrompts(caseTemplate, culpritId) {
  const casePrompts = PROMPT_MAP[caseTemplate.id]
  const prompts = {}

  for (const suspect of caseTemplate.suspects) {
    const isCulprit = suspect.id === culpritId
    const suspectPrompts = casePrompts[suspect.id]
    const scenarioPrompt = isCulprit ? suspectPrompts.culprit : suspectPrompts.innocent

    prompts[suspect.id] = `你是${suspect.name}，${suspect.age}岁，${suspect.role}。
${suspect.relationToVictim}
你的性格：${suspect.personality}

【当前案件背景】
${caseTemplate.victim.name}（${caseTemplate.victim.role}）于${caseTemplate.victim.time}在${caseTemplate.victim.location}死亡。死因为${caseTemplate.victim.deathCause}。

【你在这起案件中的情况】
${scenarioPrompt}

${BEHAVIOR_RULES}`
  }

  return prompts
}
